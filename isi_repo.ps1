# 1. Inisialisasi folder lokal menjadi repositori Git
git init

# 2. Tambahkan semua file lokal yang sudah ada
git add .

# 3. Buat commit pertama
git commit -m "Commit pertama dari lokal"

# 4. Ubah nama branch utama menjadi main
git branch -M main

# 5. Hubungkan folder lokal dengan tautan GitHub yang Anda salin tadi
# GANTI tautan di bawah ini dengan tautan milik Anda!
git remote add origin 

https://github.com/shafi182/SixSeven.git



# 6. Push semua file ke GitHub
git push -u origin main
